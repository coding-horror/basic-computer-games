namespace Game
{
    /// <summary>
    /// Enumerates the different actions that the player can take on each round
    /// of the fight.
    /// </summary>
    public enum Action
    {
        /// <summary>
        /// Dodge the bull.
        /// </summary>
        Dodge,

        /// <summary>
        /// Kill the bull.
        /// </summary>
        Kill,

        /// <summary>
        /// Freeze in place and don't do anything.
        /// </summary>
        Panic
    }
}
